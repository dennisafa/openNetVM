#define NF_TAG "webserver"
#define MIN(a,b) (((a)<(b))?(a):(b))

const char *www_main;
DIR *dir;

static int read_file (char *filename);

/*
 * Print a usage message
 */
static void
usage(const char *progname) {
        printf("Usage:\n");
        printf("%s [EAL args] -- [NF_LIB args] -- -d <destination> -p <print_delay>\n", progname);
        printf("%s -F <CONFIG_FILE.json> [EAL args] -- [NF_LIB args] -- [NF args]\n\n", progname);
        printf("Flags:\n");
        printf(" - `-d <dst>`: destination service ID to foward to\n");
        printf(" - `-p <print_delay>`: number of packets between each print, e.g. `-p 1` prints every packets.\n");
}

/*
 * Parse the application arguments.
 */
static int
parse_app_args(int argc, char *argv[], const char *progname) {
        int c, dir_flag = 0;

        while ((c = getopt(argc, argv, "p:")) != -1) {
                switch (c) {
                        case 'p':
                                www_main = optarg;
                                dir = opendir(www_main);
                                if (!dir) {
                                        dir_flag = 1;
                                }
                                break;
                        case '?':
                                usage(progname);
                                if (optopt == 'p')
                                        RTE_LOG(INFO, APP, "Option -%c requires an argument.\n", optopt);
                                else if (isprint(optopt))
                                        RTE_LOG(INFO, APP, "Unknown option `-%c'.\n", optopt);
                                else
                                        RTE_LOG(INFO, APP, "Unknown option character `\\x%x'.\n", optopt);
                                return -1;
                        default:
                                usage(progname);
                                return -1;
                }
        }

        if (!dir_flag) {
                RTE_LOG(INFO, APP, "Directory must be specified\n");
                return -1;
        }

        return optind;
}

static int
packet_handler(struct rte_mbuf *pkt, struct onvm_pkt_meta *meta,
               __attribute__((unused)) struct onvm_nf_local_ctx *nf_local_ctx) {
        meta->action = ONVM_NF_ACTION_DROP;
        return 0;
}

static void
msg_handler(void *msg, struct onvm_nf_local_ctx *nf_local_ctx) {
        struct nf_files *files;
        struct mtcp_epoll_event *even;
        char *filename;
        struct server_vars *serv;

        files = (struct nf_files *) msg;
        even = files->ev;
        serv = files->sv;
        filename = serv->fname;

        if (even == NULL || serv == NULL || filename == NULL) {
                printf("Invalid mTCP message\n");
                return;
        }

        switch (even->events) {
                case MTCP_EPOLLIN:
                        read_file(filename);

                case MTCP_EPOLLOUT:
                default:
                        printf("Unknown mTCP message\n");
                        break;
        }
}

static int
read_file (char *filename) {
        printf("%s\n", filename);
        return 0;

}

int
main(int argc, char *argv[]) {
        struct onvm_nf_local_ctx *nf_local_ctx;
        struct onvm_nf_function_table *nf_function_table;
        int arg_offset;

        const char *progname = argv[0];

        nf_local_ctx = onvm_nflib_init_nf_local_ctx();
        onvm_nflib_start_signal_handler(nf_local_ctx, NULL);

        nf_function_table = onvm_nflib_init_nf_function_table();
        nf_function_table->msg_handler = &msg_handler;
        nf_function_table->pkt_handler = &packet_handler;

        if ((arg_offset = onvm_nflib_init(argc, argv, NF_TAG, nf_local_ctx, nf_function_table)) < 0) {
                onvm_nflib_stop(nf_local_ctx);
                if (arg_offset == ONVM_SIGNAL_TERMINATION) {
                        printf("Exiting due to user termination\n");
                        return 0;
                } else {
                        rte_exit(EXIT_FAILURE, "Failed ONVM init\n");
                }
        }

        argc -= arg_offset;
        argv += arg_offset;

        if (parse_app_args(argc, argv, progname) < 0) {
                onvm_nflib_stop(nf_local_ctx);
                rte_exit(EXIT_FAILURE, "Invalid command-line arguments\n");
        }

        onvm_nflib_run(nf_local_ctx);

        onvm_nflib_stop(nf_local_ctx);
        printf("If we reach here, program is ending\n");
        return 0;
}
